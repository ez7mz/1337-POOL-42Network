#include <unistd.h>

void	put_str(char *str)
{
	while (*str)
	{
		write(1, str, 1);
		str++;
	}
}

int	main(int argc, char **argv)
{
	put_str(argv[argc - argc]);
	write (1, "\n", 1);
	return (0);
}
